	@ResponseBody
	@RequestMapping( value = "datatable" , produces = "application/json" )
	public DataTable datatable( @RequestBody DataTableRequest dataTableRequest, HttpServletRequest request,
		@RequestParam( value = "display" , required = false , defaultValue = "list" ) String display ) {
		
		String contextPath = request.getContextPath();
		DataTable dt = new DataTable();
		GenericDaoListOptions options = dataTableRequest.getGenericDaoListOptions();

		try {

			Integer count = invDaoService.getPropertyService().count( options );
            List<Property> propertyList = invDaoService.getPropertyService().list( options );
            
			List<LinkedHashMap<String, Object>> data = new ArrayList<LinkedHashMap<String, Object>>();

			for( Property property : propertyList ){

				LinkedHashMap<String, Object> tableRow = new LinkedHashMap<String, Object>();

				for ( DataTableColumn column : dataTableRequest.getColumns() ) {

					String headerName = column.getName();
					String dataName = column.getData();

					if( StringUtils.equals( "propertyId", headerName ) ){
						tableRow.put( dataName, property.getPropertyId() );
					} else if( StringUtils.equals( "name", headerName ) ){
						tableRow.put( dataName, property.getName() );
					} else if( StringUtils.equals( "description", headerName ) ){
						tableRow.put( dataName, property.getDescription() );
					} else if( StringUtils.equals( "createdBy", headerName ) ){
						tableRow.put( dataName, property.getCreatedBy() );
					} else if( StringUtils.equals( "created", headerName ) ){
						tableRow.put( dataName, property.getCreatedString() );
					} else if( StringUtils.equals( "updatedBy", headerName ) ){
						tableRow.put( dataName, property.getUpdatedBy() );
					} else if( StringUtils.equals( "updated", headerName ) ){
						tableRow.put( dataName, property.getUpdatedString() );
					} else if( StringUtils.equals( "tasks", headerName ) ){
						tableRow.put( dataName, property.getTasks().size() );
					} else if( StringUtils.equals( "location", headerName ) ){
						if( property.getLocation() != null ){
							tableRow.put( dataName, property.getLocation().toString() );
						}
					} else if( StringUtils.equals( "urls", headerName ) ) {
						String urls = "";
						if( StringUtils.equals( "list", display ) ){
							urls += "<a href=\"" + contextPath + "/property/show?"+"propertyId="+property.getPropertyId()+"\"><span class=\"glyphicon glyphicon-eye-open\"></a>";
							urls += "<a href=\"" + contextPath + "/property/edit?"+"propertyId="+property.getPropertyId()+"\"><span class=\"glyphicon glyphicon-pencil\"></a>";
							urls += "<a href=\"" + contextPath + "/property/delete?"+"propertyId="+property.getPropertyId()+"\"><span class=\"glyphicon glyphicon-trash\"></a>";
						} else {

						}
						tableRow.put( dataName, urls );
					} else {
						tableRow.put( "error", "[error: column " + headerName + " not supported]" );
					}
				}
				data.add( tableRow );
			}

			dt.setDraw( dataTableRequest.getDraw() );
            dt.setRecordsFiltered( count );
            dt.setRecordsTotal( count );
			dt.setData( data );

		} catch ( Exception e ) {
			log.error( "error builing datatable json object for Property", e );
			return datatableError( e, dataTableRequest.getDraw() );
		}
		
		return dt;
	}

