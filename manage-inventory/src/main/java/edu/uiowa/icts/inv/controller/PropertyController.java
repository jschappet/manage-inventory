    @RequestMapping( value = "save", method = RequestMethod.POST )
    public String save(@Valid @ModelAttribute( "property" ) Property property, BindingResult result, Model model ) {


		if (result.hasErrors()) { 
					model.addAttribute( "locationList", invDaoService.getLocationService().list() );

			return "/inv/property/edit"; 
		} else {
			if (property.getCreated() == null) {
				property.setCreated(new Date());
				property.setCreatedBy(getUsername());
			} else {
				property.setUpdated(new Date());
				property.setUpdatedBy(getUsername());

			}
				
			try {
				invDaoService.getPropertyService().saveOrUpdate( property );
			} catch (NonUniqueObjectException e) {
				log.debug("Merging Results");
				invDaoService.getPropertyService().merge( property );
			}
		}
		return "redirect:/property/list";
    }

